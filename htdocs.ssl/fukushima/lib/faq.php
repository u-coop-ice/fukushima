<h3>サインイン・アカウント環境について</h3>

<dl>
<dt>Q. アカウントのパスワードを忘れてしまいました。</dt>

<dd><span class="marker yellow">サインアウトした状態で</span> サインイン画面から「パスワードを忘れた方」を選択。サインインID（登録されたEmail）を入力し、「送信する」をクリックしてください。パスワード再発行手続きのURLがメールで届きます。</dd>
<dd>パスワード再発行手続きのURLへアクセスしパスワードを再設定してください。</dd>
</dl>

<dl>
<dt>Q. 退会したいのですが。</dt>
<ol>
<li>サインインした状態で「ユーザーページ」から「登録情報の確認・変更」をクリック。</li>
<li>登録情報の最下段右側の「アカウントを削除し退会する」をクリック。</li>
<li>パスワードを入力し退会手続きを完了。<br />※一度退会すると登録時に入力した情報など全てのデータが削除されますのでご注意ください。継続中のお申込み・ご注文がある場合は、それらの内容を当サイトから確認できなくなることにご留意ください。</li>
</ol>
</dl>

<dl>
<dt>Q. 登録したEmailを変更したいのですが。</dt>
<ol>
<li>サインインした状態で「ユーザーページ」から「登録情報の確認・変更」をクリック。</li>
<li>登録情報の主に生協との連絡に使用するE-mailアドレスの「メールアドレスを変更」をクリック。</li>
<li>変更したいメールアドレスを入力、「送信する」をクリックしてください。入力されたアドレスに受信確認のメールが届きます。</li>
<li>そのメールに記載された受信確認のためのURLにアクセスしメールアドレス変更手続きを完了させてください。<strong class="red">サインインの際のユーザー名も変更になります。</strong></li>
</ol>
</dl>

<dl>
<dt>Q. アルバイト求人掲載企業としてユーザー登録したのですが、サインインできません。</dt>
<dd>
<p><a class="btn btn-primary btn-sm" href="/cmp/arbeit/">アルバイト求人情報 企業さま専用ページ<i class="fa fa-fw fa-chevron-right"></i></a>からサインインください。</p>
</dd>
</dl>

<h3>機能について</h3>

<dl>
<dt>Q. お申込み内容の確認を確認したいのですが。</dt>
<dd>サインインした状態で「ユーザーページ」から「お申込み内容の確認」をクリックするとお申込み履歴一覧およびお申込みの内容にアクセスできます。</dd>
</dl>

<dl>
<dt>Q. WEBから大学生協との連絡をするにはどのような方法がありますか？</dt>
<dd>サインインした状態で「ユーザーページ」から「大学生協との連絡」をクリックすると「大学生協との連絡」履歴および内容にアクセスできます。</dd>
<dd>「返信する」ボタンがあるものは、そこから大学生協宛に返信することができます。</dd>
</dl>


