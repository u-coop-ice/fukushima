<li>過剰な金銭補償の要求</li>
<li>役職員に関する解雇等の処罰の要求</li>
<li>合理的な理由のない長時間の対応・拘束する行為</li>
<li>合理的な理由のない事業所以外の場所への呼び出しの要求</li>
<li>合理的な理由がない謝罪の要求</li>
<li>暴力行為や身体的な攻撃</li>
<li>威迫、脅迫、威嚇的な行為</li>
<li>大きな怒鳴り声をあげる行為、侮辱的発言、名誉棄損、人格否定などの行為</li>
