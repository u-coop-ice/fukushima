<h2>カスタマーハラスメントに対する方針</h2>

<p class="h1 center bold green">はじめに</p>
<p>福島大学生協は、「協同」「協力」「自立」「参加」の4つを使命とし、組合員、当組合役職員、全てのステークホルダーに対しあらゆる事業活動を通じてお役立ちすることを信条としています。今後も組合員の皆さまに、より質の高いサービスを提供し続けるために当組合役職員が心身ともに健康で安心して働き続ける職場環境を作ることが重要であると考えています。その一環としてこの度、カスタマーハラスメントに対する対応方針を策定することといたしました。（2025年1月当組合理事会議決）</p>


<p class="h1 center bold green">基本方針</p>
<p>私たちは、いつも組合員の皆さまの目線で寄り添い、ご満足いただける商品・サービスの提供を目指します。そのため、組合員の皆さまからのご意見・ご指摘には常に誠実に向き合い真摯に対応します。一方、カスタマーハラスメントに対しては、当組合役職員の人権および就業環境を著しく害し、心身の健康を害する恐れもあることから毅然と行動し、組織的に対応します。</p>


<p class="h1 center bold green">カスタマーハラスメントの<br class="visible-xs">定義</p>
<p>厚生労働省発行の「カスタマーハラスメント対策企業マニュアル」を踏まえ、「組合員および当組合の利用者」からのクレーム・言動のうち、当該クレーム・言動の要求内容の妥当性に照らして、当該要求を実現するための手段・態様が不適切なものであって、当該手段・態様により役職員の就業環境が害されるもの」をカスタマーハラスメントと定義いたします。</p>


<p class="h1 center bold green">対象となる行為例</p>
<div class="box_gray">
<ol class="calender_list back_green">
