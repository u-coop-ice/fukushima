		</ol>


<h5>（損害賠償）</h5>
	<ol>
		<li>本講座の実施に際し、申込者に対して生じた負傷・盗難等の損害については、原則として当組合は責任を負いません。但し、当組合の責めに帰すべき事由があった場合は、当講座の受講料を限度としてこれを賠償します。</li>
		<li>但し、当組合に故意または重大な過失があった場合はこの限りではありません。</li>
		</ol>

<h5>（講座の閉鎖）</h5>
	<ol>
		<li>当組合は必要と認めた場合、本講座を中止することが出来ます。</li>
		<li>この場合、申込者は9-2項に準じた受講料の返還を受けることができます。その際、当組合は違約金及び解約手数料を収受することはありません。</li>
		</ol>

<h5>（紛争の解決）</h5>
	<ol>
		<li>本規定に定める自公及び、当該契約について疑義が生じた場合は、申込者と当組合とで誠意を持って協議し、解決するものとします。</li>
