<h3>公務員講座 規定</h3>

<p class="pad_l">福島大学生協オリジナル公務員本講座および福島大学生協オリジナル公務員準備講座のお申し込みにあたっては、事前に下記事項を必ずご確認いただきますようお願い致します。</p>

<h5>（適用範囲）</h5>
	<ol>
		<li>本規定は福島大学生活協同組合（以下当組合という）が実施する福島大学生協オリジナル公務員本講座および福島大学生協オリジナル公務員準備講座（以下本講座という）に適用される契約条件を定めたものです。本規定に定めのない事項については、当該の申込書類等（以下申込書類という）の定めによるものとします。</li>
		<li>各講座に付随するオプション講座についても本規定を適用するものとします。</li>
	</ol>

<h5>（契約の成立）</h5>
	<ol>
		<li>本講座の申込者（以下申込者という）は、本規定および受講案内の内容を承諾の上、当組合に対して受講の申込みを行い、当組合がこれを受諾した時点で受講契約が成立するものとします。</li>
	</ol>

<h5>（受講料の支払い）</h5>
	<ol>
		<li>申込者は受講案内に記載された受講料、教材費等の費用（以下受講費用という）を、当組合が指定した方法により、当組合が指定した期日までに支払うものとします。支払いがなされない場合、当組合は契約を解除することができるものとします。</li>
	</ol>

<h5>（役務の提供）</h5>
	<ol>
		<li>当組合は申込者に対して受講案内に記載した役務を提供するものとします。</li>
		<li>ただし、当初の予定通りに役務が提供できない場合には、速やかに代替措置を講じます。</li>
	</ol>

<h5>（受講開始日）</h5>
	<ol>
		<li>本講座の受講開始日は、申込者の受講の有無にかかわらず、申込書類に記載された日付とします。</li>
	</ol>

<h5>（実施場所）</h5>
	<ol>
		<li>本講座の実施場所は、申込書類で定めるものとします。</li>
	</ol>

<h5>（提供する役務の変更）</h5>
	<ol>
		<li>当組合は、事前に申込者に告知することで本講座の受講日及び実施場所、提供する役務の軽微な内容を変更することができるものとします。</li>
	</ol>

<h5>（受講期間・回数・形態）</h5>
	<ol>
		<li>本講座の受講期間、回数、形態、その他の諸条件(最少人数など)は、申込書類に記載するものとし、申込者は、申込書類に記載された受講期間及び回数に限り受講できるものとします。</li>
	</ol>

<h5>（撮影・録音）</h5>
	<ol>
		<li>当組合は、講座の撮影・録音を行えるものとします。</li>
		<li>撮影・録音した画像・音声は、講座事務局が管理し、講座の品質向上のため使用できるものとします。</li>
		<li>申込者の同意がある場合に限り、当組合は、撮影・録音した画像・音声を、普及広報の目的で使用できるものとします。</li>
		<li>申込者は、前項の同意をした場合であっても、書面と提出することにより、撮影・録音した画像・音声の利用の停止をできるものとします。</li>
	</ol>


