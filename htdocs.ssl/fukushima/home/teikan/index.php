<h4>（財務処理）</h4>
<table>
<tr><td>第70条</td><td>この組合は、法令及びこの組合の経理に関する規則の定めるところにより、この組合の財務の処理を行い、決算関係書類及びその附属明細書を作成するものとする。</td></tr>
</table>

<h4>（収支の明示）</h4>
<table>
<tr><td>第71条</td><td>この組合は、この組合が行う事業の種類ごとに収支を明らかにするものとする。</td></tr>
</table>

<h4>（法定準備金）</h4>
<table>
<tr><td>第72条</td><td>この組合は、出資総額の2分の1に相当する額に達するまで、毎事業年度の剰余金の10分の1に相当する額以上の金額を法定準備金として積み立てるものとする。ただし、この場合において繰越欠損金があるときには、積み立てるべき準備金の額の計算は、当該事業年度の剰余金からその欠損金のてん補に充てるべき金額を控除した額について行うものとする。
<ol start="2">
<li>前項の規定による法定準備金は、欠損金のてん補に充てる場合を除き、取り崩すことができない。</li>
</ol>
</td></tr>
</table>

<h4>（教育事業等繰越金）</h4>
<table>
<tr><td>第73条</td><td>この組合は、毎事業年度の剰余金の20分の1に相当する額以上の金額を 教育事業等繰越金として翌事業年度に繰り越し、繰り越された事業年度の第3条第5号に定める事業の費用に充てるために支出するものとする。なお、全部又は一部を組合員の相互の協力の下に地域及びこの組合の区域において行う福祉の向上に資する活動を助成する事業に充てることができる。
<ol start="2">
<li>前条第1項ただし書の規定は、前項の規定による繰越金の額の計算について準用する。</li>
</ol>
</td></tr>
</table>

<h4>（剰余金の割戻し）</h4>
<table>
<tr><td>第74条</td><td>この組合は、剰余金について、組合員の組合事業の利用分量又は払込んだ出資額に応じて組合員に割り戻すことができる。</td></tr>
</table>

<h4>（利用分量に応ずる割戻し）</h4>
<table>
<tr><td>第75条</td><td>組合事業の利用分量に応ずる剰余金の割戻し(以下「利用分量割戻し」という。)は、毎事業年度の剰余金について、繰越欠損金をてん補し、第72条第1項の規定による法定準備金として積み立てる金額及び第73条第1項の規定による教育事業等繰越金として繰り越す金額(以下「法定準備金等の金額」という。)を控除した後に、なお残余があるときに行うことができる。
<ol start="2">
<li>利用分量割戻しは、各事業年度における組合員の組合事業の利用分量に応じて行う。</li>
<li>この組合は、組合事業を利用する組合員に対し、組合事業の利用の都度利用した事業の分量を証する領収書、レシート等を交付するものとする。</li>
<li>この組合は、組合員が利用した組合事業の利用分量の総額がこの組合の事業総額の5割以上であると確認した場合でなければ、利用分量割戻しを行わない。</li>
<li>この組合は、利用分量割戻しを行うこと及び利用分量割戻金の額について総代会の議決があったときは、速やかに利用分量割戻金の利用分量に対する割合及び利用分量割戻金の請求方法を組合員に公告するものとする。</li>
<li>この組合は、利用分量割戻しを行うときは、その割り戻すべき金額に相当する額を利用分量割戻金として積み立てるものとする。</li>
<li>組合員は、第5項の公告に基づき利用分量割戻金をこの組合に請求しようとするときは、利用分量割戻しを行うことについての議決が行われた総代会の終了の日から6箇月を経過する日までに、第3項の規定により交付を受けた領収書、レシート等を提出してこれをしなければならない。</li>
<li>この組合は、前項の請求があったときは、第6項の規定による利用分量割戻金の積立てを行った事業年度の翌々事業年度の末日までに、その利用分量割戻金を取り崩して、組合員ごとに前項の規定により提出された領収書、レシート等によって確認した事業の利用分量に応じ、利用分量割戻金を支払うものとする。</li>
<li>この組合は、各組合員ごとの利用分量があらかじめ明らかである場合には、第7項の規定にかかわらず、組合員からの利用分量割戻金の請求があったものとみなして、前項の支払を行うことができる。</li>
<li>この組合が、前2項の規定により利用分量割戻しを行おうとする場合において、この組合の責めに帰すべき事由以外の事由により第8項に定める期間内に支払を行うことができなかったときは、当該組合員は、当該期間の末日をもって利用分量割戻金の請求権を放棄したものとみなす。</li>
<li>この組合は、各事業年度の利用分量割戻金のうち、第8項に定める期間内に割戻しを行うことができなかった額は、当該事業年度の翌々事業年度における事業の剰余金に算入するものとする。</li>
</ol>
</td></tr>
</table>

<h4>（出資額に応ずる割戻し）</h4>
<table>
<tr><td>第76条</td><td>払い込んだ出資額に応ずる剰余金の割戻し(以下「出資配当」という。)は、毎事業年度の剰余金から法定準備金等の金額を控除した額又は当該事業年度の欠損金に、繰越剰余金又は繰越欠損金を加減し、さらに任意積立金取崩額を加算した額について行うことができる。
<ol start="2">
<li>出資配当は、各事業年度の終わりにおける組合員の払込済出資額に応じて行う。</li>
<li>出資配当金の額は、払込済出資額につき年1割以内の額とする。</li>
<li>この組合は、出資配当を行うこと及び出資配当金の額について総代会の議決があったときは、速やかに出資配当金の払込済出資額に対する割合及び出資配当金の請求方法を組合員に公告するものとする。</li>
<li>組合員は、前項の公告に基づき出資配当金をこの組合に請求しようとするときは、出資配当を行うことについての議決が行われた総代会の終了の日から6箇月を経過する日までにこれをしなければならない。</li>
<li>この組合は、前項の請求があったときは、遅滞なく出資配当金を支払うものとする。</li>
<li>この組合は、あらかじめ支払方法を明確に定めている場合には、第5項の規定にかかわらず、組合員からの出資配当金の請求があったものとみなして、前項の支払を行うことができる。</li>
<li>この組合が、前2項の規定により出資配当金の支払を行おうとする場合において、この組合の責めに帰すべき事由以外の事由により支払を行えなかったときは、第4項に定める総代会の終了の日から2年を経過する日までの間に請求を行った場合を除き、当該組合員は、出資配当金の請求権を放棄したものとみなす。</li>
</ol>
</td></tr>
</table>

