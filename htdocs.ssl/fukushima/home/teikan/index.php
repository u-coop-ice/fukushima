<ol start="2">
<li>第3条第2号に規定する生活に有用な協同施設の種類は、食堂、喫茶その他の生活に必要な協同施設とする。</li>
<li>第3条第3号に規定する生活の改善及び文化の向上を図る事業とは、アルバイト紹介、職業紹介事業などによる生活改善事業とする。また、文化の向上を図る事業とは、各種講演会、映画会、音楽会などの活動とする。</li>
